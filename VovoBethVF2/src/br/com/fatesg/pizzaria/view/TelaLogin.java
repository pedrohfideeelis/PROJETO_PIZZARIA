    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        String cpf = jTextFieldLogin.getText();
        String senha = new String(jPasswordFieldSenha.getPassword());

        Usuario usuario = LoginService.fazerLogin(cpf, senha);

        if (usuario != null) {
            JOptionPane.showMessageDialog(null, "Login bem-sucedido! Bem-vindo, " + usuario.getNome() + "!");
            //ABRE A TELA DE CARD√ÅPIO
            this.dispose();
            TelaCardapio cardapio = new TelaCardapio(usuario);
            cardapio.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Login falhou. Verifique suas credenciais.");
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

