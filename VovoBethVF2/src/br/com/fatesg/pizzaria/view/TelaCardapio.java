    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        // TODO add your handling code here:
        String selectedPizza = (String) jComboBoxPizzas.getSelectedItem();

        List<Adicionais> adicionais = new ArrayList<>();

        int quantidade = 0;
        try {
            quantidade = Integer.parseInt(jTextFieldQuantidade.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Quantidade inv√°lida.");
            return;
        }

        FabricaPizza.TipoPizza tipoPizza = FabricaPizza.TipoPizza.valueOf(selectedPizza.toUpperCase());
        Pizza pizza = new FabricaPizza().criarPizza(tipoPizza);

        if (jCheckBoxAbacaxi.isSelected()) {
            pizza = new Abacaxi(pizza);
        }
        if (jCheckBoxBacon.isSelected()) {
            pizza = new Bacon(pizza);
        }
        if (jCheckBoxCebola.isSelected()) {
            pizza = new Cebola(pizza);
        }

        PedidoItem item = new PedidoItem(pizza, quantidade);

        carrinho.add(item);

        jComboBoxPizzas.setSelectedIndex(0);
        jCheckBoxAbacaxi.setSelected(false);
        jCheckBoxBacon.setSelected(false);
        jCheckBoxCebola.setSelected(false);
        jTextFieldQuantidade.setText("");

        JOptionPane.showMessageDialog(null, "Pizza adicionada ao carrinho.");
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

    private void jButtonFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFinalizarActionPerformed
        // TODO add your handling code here:
        StringBuilder resumoCarrinho = new StringBuilder("Resumo do Carrinho:\n\n");
        double valorTotal = 0.0;

        for (PedidoItem pedidoItem : carrinho) {
            Pizza pizza = pedidoItem.getPizza();
            int quantidade = pedidoItem.getQuantidade();

            double valorItem = pizza.cost() * quantidade;
            valorTotal += valorItem;

            resumoCarrinho.append("Pizza: ").append(pizza.getDescription());
            resumoCarrinho.append(" (Quantidade: ").append(quantidade).append(")\n");
            resumoCarrinho.append("Valor do Item: R$").append(valorItem).append("\n\n");
        }

        resumoCarrinho.append("Valor Total do Pedido: R$").append(valorTotal);
        JOptionPane.showMessageDialog(null, resumoCarrinho.toString());

//        try {
//            Document document = new Document();
//            PdfWriter.getInstance(document, new FileOutputStream("resumo_pedido.pdf"));
//            document.open();
//            document.add(new Paragraph(resumoCarrinho.toString()));
//            document.close();
//            JOptionPane.showMessageDialog(null, "Resumo do pedido salvo em resumo_pedido.pdf.");
//        } catch (DocumentException | FileNotFoundException e) {
//            e.printStackTrace();
//            JOptionPane.showMessageDialog(null, "Erro ao salvar o resumo do pedido em PDF.");
//        }

        carrinho.clear();

        String descricaoPedido = resumoCarrinho.toString();

        int usuarioId = this.usuarioLogado.getId();

        PedidoService.inserirPedido(usuarioId, descricaoPedido, valorTotal);
        this.dispose();
    }//GEN-LAST:event_jButtonFinalizarActionPerformed

